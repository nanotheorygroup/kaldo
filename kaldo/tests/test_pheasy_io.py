"""
Tests for the pheasy force-constant importer (format='pheasy').

Unit tests build synthetic files in tmp_path following pheasy's exact output
conventions (see kaldo/interfaces/pheasy_io.py docstring). Integration tests
use the committed differential fixtures under si-crystal/pheasy and
mgo/pheasy, generated by their make_fixture.py from the trusted hiphive and
qe-d3q routes.
"""
from pathlib import Path

import numpy as np
import pytest

POSCAR_2ATOM = (
    "Si\n1.0\n3.0 0.0 0.0\n0.0 3.0 0.0\n0.0 0.0 3.0\nSi\n2\n"
    "Direct\n0.0 0.0 0.0\n0.5 0.5 0.5\n"
)


def _write_poscar(folder):
    (Path(folder) / "POSCAR").write_text(POSCAR_2ATOM)


def test_structure_missing_raises(tmp_path):
    from kaldo.interfaces.pheasy_io import read_pheasy_structure
    with pytest.raises(FileNotFoundError, match=r"POSCAR.*cell\.in|cell\.in.*POSCAR"):
        read_pheasy_structure(str(tmp_path))


def test_structure_reads_poscar(tmp_path):
    from kaldo.interfaces.pheasy_io import read_pheasy_structure
    _write_poscar(tmp_path)
    atoms = read_pheasy_structure(str(tmp_path))
    assert atoms.positions.shape == (2, 3)
    assert atoms.get_chemical_symbols() == ["Si", "Si"]


def test_born_charges_roundtrip(tmp_path):
    from kaldo.interfaces.pheasy_io import read_born_charges
    dielectric = np.diag([2.5, 2.5, 2.5])
    born_0 = np.diag([1.1, 1.1, 1.1])
    born_1 = np.diag([-1.1, -1.1, -1.1])
    rows = np.vstack([dielectric, born_0, born_1])
    np.savetxt(tmp_path / "born.fmt", rows)
    charges = read_born_charges(str(tmp_path), n_atoms=2)
    assert charges.shape == (3, 3, 3)
    np.testing.assert_allclose(charges[0], dielectric, atol=1e-12)
    np.testing.assert_allclose(charges[1], born_0, atol=1e-12)
    np.testing.assert_allclose(charges[2], born_1, atol=1e-12)


def test_born_charges_wrong_count_raises(tmp_path):
    from kaldo.interfaces.pheasy_io import read_born_charges
    np.savetxt(tmp_path / "born.fmt", np.ones((8, 3)))  # 9 rows expected for 2 atoms
    with pytest.raises(ValueError, match="born.fmt"):
        read_born_charges(str(tmp_path), n_atoms=2)


def test_pheasy_cell_map_is_exact_on_asymmetric_grid():
    from kaldo.interfaces.pheasy_io import _pheasy_cell_to_kaldo_id
    d0, d1, d2 = 2, 3, 4
    mapping = _pheasy_cell_to_kaldo_id((d0, d1, d2))
    assert mapping.shape == (24,)
    assert sorted(mapping.tolist()) == list(range(24))
    for c in range(24):
        ox = c % d0
        oy = (c // d0) % d1
        oz = c // (d0 * d1)
        kaldo_c_order_id = ox * (d1 * d2) + oy * d2 + oz
        assert mapping[c] == kaldo_c_order_id


def test_supercell_order_check_accepts_pheasy_order(tmp_path, caplog):
    import ase.io
    from ase import Atoms
    from kaldo.interfaces.pheasy_io import check_pheasy_supercell_order
    _write_poscar(tmp_path)
    atoms = ase.io.read(tmp_path / "POSCAR", format="vasp")
    d0, d1, d2 = 2, 1, 1
    n_cells = d0 * d1 * d2
    scaled = atoms.get_scaled_positions()
    positions, numbers = [], []
    for i in range(len(atoms)):
        for c in range(n_cells):
            ox, oy, oz = c % d0, (c // d0) % d1, c // (d0 * d1)
            positions.append((scaled[i] + np.array([ox, oy, oz])) / np.array([d0, d1, d2]))
            numbers.append(atoms.numbers[i])
    sposcar = Atoms(numbers=numbers, scaled_positions=positions,
                    cell=np.array(atoms.cell) * np.array([[d0], [d1], [d2]]), pbc=True)
    ase.io.write(tmp_path / "SPOSCAR", sposcar, format="vasp")
    check_pheasy_supercell_order(str(tmp_path), atoms, (d0, d1, d2))
    assert not [r for r in caplog.records if "pheasy supercell" in r.getMessage()
                or "atom order" in r.getMessage()]


def test_supercell_order_check_warns_on_cell_major_order(tmp_path, caplog):
    import ase.io
    from kaldo.interfaces.pheasy_io import check_pheasy_supercell_order
    _write_poscar(tmp_path)
    atoms = ase.io.read(tmp_path / "POSCAR", format="vasp")
    wrong = atoms * (2, 1, 1)  # ASE repeat is cell-major, not pheasy's atom-major
    ase.io.write(tmp_path / "SPOSCAR", wrong, format="vasp")
    check_pheasy_supercell_order(str(tmp_path), atoms, (2, 1, 1))
    assert [r for r in caplog.records if "atom order does not match" in r.getMessage()]


def test_supercell_order_check_noop_without_supercell_file(tmp_path, caplog):
    import ase.io
    from kaldo.interfaces.pheasy_io import check_pheasy_supercell_order
    _write_poscar(tmp_path)
    atoms = ase.io.read(tmp_path / "POSCAR", format="vasp")
    check_pheasy_supercell_order(str(tmp_path), atoms, (2, 1, 1))
    assert not caplog.records
