    dense_negative = ifc4_negative.todense()
    np.testing.assert_allclose(dense_negative, dense_positive, atol=1e-12)
    np.testing.assert_allclose(dense_negative[0, :, 1, 1, :, 0, 0, :, 0, 1, :], phi, atol=1e-12)


