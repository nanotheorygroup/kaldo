    negative_file = tmp_path / "FORCE_CONSTANTS_4TH_NEG"
    _write_fc4_text(positive_file, [(np.array([3.0, 0.0, 0.0]), Z, Z, (0, 1, 0, 1), phi)])
    _write_fc4_text(negative_file, [(np.array([-3.0, 0.0, 0.0]), Z, Z, (0, 1, 0, 1), phi)])
